export function makeServer({ environment = "test" }) {
  const server = createServer({
    environment,

    models: {
      continent: Model,
    },

    seeds(server) {
      server.db.loadData({
        continents,
      });
    },

    routes() {
      this.namespace = "api";

      this.get("/continents", () => {
        return this.schema.all("continent");
      });
    },
  });

  return server;
}
